package org.example.OOP.Zadanie2;

public class Garden {
    private String name;
    private String infoOfPlant;
    private String dailyAmountOfWater;
    private String weeklyAmountOfWater;
    private String yearlyAmountOfWater;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setInfoOfPlant(String infoOfPlant) {
        this.infoOfPlant = infoOfPlant;
    }

    public String getInfoOfPlant() {
        return infoOfPlant;
    }

    public void setDailyAmountOfWater(String dailyAmountOfWater) {
        this.dailyAmountOfWater = dailyAmountOfWater;
    }

    public String getDailyAmountOfWater() {
        return dailyAmountOfWater;
    }

    public void setWeeklyAmountOfWater(String weeklyAmountOfWater) {
        this.weeklyAmountOfWater = weeklyAmountOfWater;
    }

    public String getWeeklyAmountOfWater() {
        return weeklyAmountOfWater;
    }

    public void setYearlyAmountOfWater(String yearlyAmountOfWater) {
        this.yearlyAmountOfWater = yearlyAmountOfWater;
    }
    public String getYearlyAmountOfWater(){
        return yearlyAmountOfWater;
    }
}
